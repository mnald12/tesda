const mac = (center, address, email, number, qualifications) => {

	qualifications.sort((a, b) => {
		if(a < b) return -1
		else return 1
		return 0
	}); 

	let conts = ''
	for(let i of qualifications){
		conts = conts + `<li>${i.name} - <strong>Fee : ₱ ${i.fee}</strong></li>`
	}

	return `
		<div class="modal-dialog modal-xl modal-dialog-centered modal-dialog-scrollable">
            <div class="modal-content">
               <div class="modal-header">
                  	<h4 class="modal-title" id="exampleModalLabel">Assessment Center</h4>
                  	<button type="button" class="btn-close" data-bs-dismiss="modal" aria-label="Close"></button>
               </div>
               <div class="modal-body">
                  <div class="alert alert-dark" role="alert">
                     <h2>${center}</h2>
                     <h6>
                        Location : <strong>${address}</strong>
                     </h6>
                     <h6>Email : <strong>${email}</strong></h6>
                     <h6>Contact : <strong>${number}</strong></h6>
                  </div>
                  <div class="alert alert-light" role="alert">
                  	<h3>Qualifications</h3>
                     ${conts}
                  </div>
               </div>
            </div>
         </div>
	`
}

let ass =  []

for(let i = 0; i < assessmemnt_centers.length; i++){
	ass.push({id: i, el : mac(assessmemnt_centers[i].center, assessmemnt_centers[i].address, assessmemnt_centers[i].email, assessmemnt_centers[i].number, assessmemnt_centers[i].qualifications)}) 
}

for(let i of ass){
	const md = document.createElement('div')
	md.classList.add('modal')
	md.classList.add('fade')
	md.setAttribute('tabindex', '-1')
	md.setAttribute('aria-labelledby', 'exampleModalLabel')
	md.ariaHidden = true
	md.id = 'ac'+i.id
	md.innerHTML = i.el
	document.body.appendChild(md)
}